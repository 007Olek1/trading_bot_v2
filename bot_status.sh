#!/bin/bash

echo "================================================================================"
echo "📊 СТАТУС ТОРГОВОГО БОТА"
echo "================================================================================"
echo ""

# Проверяем процесс
BOT_PROCESS=$(ps aux | grep -i "python.*main.py" | grep -v grep)

if [ -n "$BOT_PROCESS" ]; then
    echo "✅ Бот РАБОТАЕТ"
    echo ""
    echo "$BOT_PROCESS" | awk '{print "   PID: " $2 "\n   CPU: " $3 "% \n   MEM: " $4 "% \n   Запущен: " $9}'
    echo ""
    
    # Показываем последние логи
    if [ -f logs/trading_bot_v4.log ]; then
        echo "📋 Последние 10 строк лога:"
        echo "--------------------------------------------------------------------------------"
        tail -10 logs/trading_bot_v4.log
        echo "--------------------------------------------------------------------------------"
    fi
else
    echo "🔴 Бот НЕ РАБОТАЕТ"
    echo ""
    
    # Проверяем последние логи на ошибки
    if [ -f logs/bot_output.log ]; then
        echo "📋 Последние ошибки из лога:"
        echo "--------------------------------------------------------------------------------"
        tail -20 logs/bot_output.log | grep -i "error\|exception\|traceback" || echo "   Нет ошибок в последних записях"
        echo "--------------------------------------------------------------------------------"
    fi
fi

echo ""
echo "================================================================================"
